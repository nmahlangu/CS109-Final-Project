		<u>The Basics</u>
			<ul>
			<li>Depending on the limit and betting structure, players will place out blinds and antes so there is an initial amount to get this started. This is called <i>posting</i>.</li>
			<li>The dealer shuffles up a standard deck of 52 playing cards.</li>
			<li>Each player is dealt 2 private cards face down. These are called your <i>hole cards</i> or <i>pocket cards</i>.</li>
			<li>Then there is a round of betting starting with the player to the left of the blinds. This is the <i>preflop</i> betting round. Like most games of poker, players can call (match a bet or a raise), raise (increase the size of an existing bet in the same betting round), or fold (discard one's hand and forfeit interest in the current pot). </li>
			<li>After the betting round ends, the dealer discards the top card of the deck. This is called the <i>burn card</i>. This is done to prevent cheating.</li>
			<li>The dealer then flips the next three cards face up on the table. This is called <i>the flop</i>. These are communal cards that anyone can use in combination with their two pocket cards to form a poker hand.</li>
			<li>The player to the left of the dealer starts another betting round.</li>
			<li>After the betting concludes, the dealer burns again then flips another communal card onto the table. This is called the <i>turn</i>.</li>
			<li>The player to the left of the dealer starts another betting round. In many types of games, this is where the bet size doubles.</li>
			<li>Again, the dealer burns a card and places a final card face up on the table. This is called the <i>river</i>. Players can now use any of the five cards on the table or the two cards in their pocket to form a five card poker hand.</li>
			<li>There is one final round of betting starting with the player to the left of the dealer</li>
			<li>After that, we have the <i>showdown</i>. Players who have not folded revealed their hands, beginning with the player to the left of the last player to call. Players use a combination of their pocket cards and the community cards to form a five card poker hand.</li>
			<li>The player who shows the best hand wins! Although sometimes players with the same hand split the pot.</li>
		</ul>
